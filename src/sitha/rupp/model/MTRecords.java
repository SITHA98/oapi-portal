package sitha.rupp.model;

public class MTRecords {
	
	private int record_num;
	private String record_date;
	private String flexcube_date;
	private String description;
	private String tableName;
	private String created_by;
	
	public int getRecord_num() {
		return record_num;
	}
	public void setRecord_num(int record_num) {
		this.record_num = record_num;
	}
	public String getRecord_date() {
		return record_date;
	}
	public void setRecord_date(String record_date) {
		this.record_date = record_date;
	}
	public String getFlexcube_date() {
		return flexcube_date;
	}
	public void setFlexcube_date(String flexcube_date) {
		this.flexcube_date = flexcube_date;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getTableName() {
		return tableName;
	}
	public void setTableName(String tableName) {
		this.tableName = tableName;
	}
	public String getCreated_by() {
		return created_by;
	}
	public void setCreated_by(String created_by) {
		this.created_by = created_by;
	}
	
	

}
